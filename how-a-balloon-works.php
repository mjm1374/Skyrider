					<h1 class="deepblue">Balloon Components</h1>
					<h2>and some technical details</h2>
					<div class="contentbox">
						<div class="storybox">
							<p><span class="yellow">THE ENVELOPE (or "balloon")</span><br />
								is made of ripstop nylon coated with silicon or polyurethane to make it airtight. It contains the hot air and provides the lift. The bottom of the envelope is usually made of nomex, a non- flammable material.</p>
							<p>The air temperature inside the envelope can reach 200-250 degrees Fahrenheit but varies depending on the ambient temperature, payload, and altitude.</p>

							<p><span class="yellow">THE BASKET (or gondola)</span><br />
								is where you stand, enjoying the scenery. Wicker has been proven to be the best basket material because it is light, strong, durable and flexible. The basket contains the propane tanks that feed the burner.</p>

							<p><span class="yellow">INSTRUMENTS </span><br />
								include an altimeter, a variometer that measures vertical speed, and a temperature gauge for the envelope. Most pilots also use a radio and a GPS. </p>

							<p><span class="yellow">THE BURNER</span><br />
								is the 'engine' of the balloon. It uses liquid propane to produce a long flame up to 15 million btus (that is about 100 times your average barbecue grill) and heats the air inside the envelope.</p>

							<p>All systems are redundant, this means that there is 2 of everything: in case something goes wrong with one, there is full backup.</p>

							<p><span class="yellow">DIFFERENT KINDS OF BALLOONS</span><br />
								Like cars, balloons come in all shapes and sizes, from the 1-person balloon to the "bus" with over 12 passengers. They can have a regular (round) shape or a special shape like the Energizer bunny, Airabelle the cow or Josie the flying cathedral.</p>

							<p>Like cars, balloons require an annual inspection by an FAA-certified repair station.</p>
