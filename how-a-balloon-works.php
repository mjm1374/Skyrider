				<h2>Basics of balloon flying</h2>
				<div class="contentbox">

					<div class="storybox">

						<p>Hot air balloons fly because the hot air they contain is less dense thus lighter than the colder surrounding air. They are "lighter than air", actually floating over the layer of denser air, just like styrofoam floats over water, instead of truly flying like an airplane.</p>

						<p>The hotter balloons are, the higher they fly. The pilot can add heat with the burner and can also vent some hot air to cool the balloon and fly lower or land. The altitude control is actually very precise.</p>

						<p>The balloon literally goes with the wind, its direction is determined by the wind direction. The pilot steers the balloon by changing the altitude of the aircraft, catching different winds with various speeds or directions This is a very natural form of flying. </p>
